package a2o2.MC.Setting;

/**
 * 아이템 클릭 예제에 사용하는 데이터 도메인 클래스
 * 타이틀, 설명, 색상(int)의 세가지 데이터로 구성되어 있다.
 * 
 * @author croute
 * @since 2011.07.12
 */
public class Data
{
	// 타이틀
	private String title;
	
	// 설명
	private String description;
	
	// 색상
	private int color;

	/**
	 * 데이터 클래스 기본 생성자
	 */
	public Data()
	{
		super();
	}

	/**
	 * 데이터 클래스 생성자
	 * 
	 * @param title 타이틀
	 * @param description 설명
	 * @param color 색상
	 */
	public Data(String title, String description, int color)
	{
		super();
		this.title = title;
		this.description = description;
		this.color = color;
	}

	/**
	 * 색상 이름을 반환합니다.
	 * 
	 * @return
	 */
	public String getTitle()
	{
		return title;
	}

	/**
	 * 색상 이름을 저장합니다.
	 * 
	 * @param title
	 */
	public void setTitle(String title)
	{
		this.title = title;
	}

	/**
	 * 색상에 대한 설명을 반환합니다.
	 * 
	 * @return
	 */
	public String getDescription()
	{
		return description;
	}

	/**
	 * 색상에 대한 설명을 저장합니다.
	 * 
	 * @param description
	 */
	public void setDescription(String description)
	{
		this.description = description;
	}

	/**
	 * 색상값을 반환합니다.
	 * 
	 * @return
	 */
	public int getColor()
	{
		return color;
	}

	/**
	 * 색상값을 저장합니다.
	 * 
	 * @param color Color.SOMETHING
	 */
	public void setColor(int color)
	{
		this.color = color;
	}

}
